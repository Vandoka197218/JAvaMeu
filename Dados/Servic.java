package Dados;

/**
 *
 * @author Administrador
 */
public class Servic {
    private int codServ;
    private String Desc;
    private int QuantS;
    private String Setor;
    private double Valor;

    /**
     * @return the codServ
     */
    public int getCodServ() {
        return codServ;
    }

    /**
     * @param codServ the codServ to set
     */
    public void setCodServ(int codServ) {
        this.codServ = codServ;
    }

    /**
     * @return the Desc
     */
    public String getDesc() {
        return Desc;
    }

    /**
     * @param Desc the Desc to set
     */
    public void setDesc(String Desc) {
        this.Desc = Desc;
    }

    /**
     * @return the QuantS
     */
    public int getQuantS() {
        return QuantS;
    }

    /**
     * @param QuantS the QuantS to set
     */
    public void setQuantS(int QuantS) {
        this.QuantS = QuantS;
    }

    /**
     * @return the Setor
     */
    public String getSetor() {
        return Setor;
    }

    /**
     * @param Setor the Setor to set
     */
    public void setSetor(String Setor) {
        this.Setor = Setor;
    }

    /**
     * @return the Valor
     */
    public double getValor() {
        return Valor;
    }

    /**
     * @param Valor the Valor to set
     */
    public void setValor(double Valor) {
        this.Valor = Valor;
    }
    
    
}
